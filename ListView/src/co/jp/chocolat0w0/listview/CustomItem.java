package co.jp.chocolat0w0.listview;

public class CustomItem {

	private String left;
	private String right;
	
	public CustomItem(String left, String right) {
		this.left = left;
		this.right = right;
	}
	
	public String getLeft() {
		return left;
	}
	
	public String getRight() {
		return right;
	}

}
